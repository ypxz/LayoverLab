_LEVEL_PHRASE: dict[Level, str] = {
    "visa_free_majority": "visa-free for many passports",
    "visa_on_arrival": "visa on arrival for many passports",
    "evisa": "e-visa/electronic authorisation needed for most",
    "visa_required_many": "visa/authorisation required for many passports",
    "transit_without_visa_common": "visa-free transit commonly available",
}


def get_stopover_hint(country_code: str) -> str | None:
    """One-line, passport-agnostic entry hint for a stopover country, or None if unknown."""
    rule = VISA_RULES.get((country_code or "").strip().upper())
    if rule is None:
        return None
    return (
        f"{rule['country']}: {rule['note']} ({_LEVEL_PHRASE[rule['level']]}) — "
        "check rules for your passport."
    )
